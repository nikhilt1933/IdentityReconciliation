DROP TABLE IF EXISTS CONTACT;


CREATE TABLE IF NOT EXISTS Contact (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
	EMAIL VARCHAR(200),
	PHONE VARCHAR(200),
	PRECEDENCE VARCHAR(10),
	LINKEDID INTEGER,
	CREATEDON VARCHAR(50),
	UPDATEDON VARCHAR(50),
	DELETEDON VARCHAR(50),
	PRIMARY KEY (Id)
);

CREATE TABLE IF NOT EXISTS PRIMARYRECORD (
	CONTACTID INTEGER,
	PRIMARYCONTACTID INTEGER,
	PRIMARY KEY (CONTACTID)
);

CREATE TABLE IF NOT EXISTS MAPPING (
	PRIMARYCONTACTID INTEGER,
	SECONDARYMAPPING VARCHAR(300),
	PRIMARY KEY (PRIMARYCONTACTID)
);

